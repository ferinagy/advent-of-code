private fun part1(input: String): Int = solve(input, false)

private fun part2(input: String): Int = solve(input, true)

private fun solve(input: String, returnToStart: Boolean): Int {
    val lines = input.lines()

    lateinit var start: Coord2D
    val cables = mutableListOf<Coord2D>()
    val grid = BooleanGrid(lines.first().length, lines.size) { x, y ->
        val c = lines[y][x]
        if (c == '0') {
            start = Coord2D(x, y)
        } else if (c in '1' .. '9') {
            cables += Coord2D(x, y)
        }
        c == '#'
    }

    val initial = RobotPath(start, cables)

    return searchGraph(
        start = initial,
        isDone = { it.remaining.isEmpty() && (!returnToStart || it.position == start) },
        nextSteps = { current ->
            current.position.adjacent(includeDiagonals = false)
                .filter { !grid[it] }
                .map {
                    RobotPath(position = it, remaining = current.remaining - it)
                }
                .toSet().singleStep()
        }
    )
}

private data class RobotPath(val position: Coord2D, val remaining: List<Coord2D>)

